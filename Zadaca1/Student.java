public class Student {
    String ime;
    String prezime;
    int indeks;

    public Student() {
        this.ime = ime;
        this.prezime = prezime;
        this.indeks = indeks;
    }
    public Student(String ime, String prezime, int indeks) {
        this.ime = ime;
        this.prezime = prezime;
        this.indeks = indeks;
    }
}
